//Looking at quirky things with variables 


package com.bayviewglen.daythree;

public class Example3 {

	public static void main(String[] args) {
		int x = 1; //Declared x and initialized it in the same line
		int y = 2;
		System.out.println("" + x + y + " are my variables"); //Empty string removes math
		System.out.println(x + y + " are my variables"); //No change allows math
		System.out.println(x + " " + y + " are my variables"); //Empty String in middle: no math
		System.out.println(x + (x + " are my variables")); //Included variable in brackets




	}

}
